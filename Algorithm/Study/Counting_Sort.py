# ==================================================
# 카운팅 정렬 사고 순서 정리
# ==================================================
# 목적:
# - 값 비교 없이
# - "개수 → 자리 → 배치"로 정렬하기
#
# 사용 조건:
# - 값의 범위가 작고 명확함
# - 정렬된 결과가 필요함
# ==================================================


# 0️⃣ 값의 범위 확인
# 예: 값이 0 ~ 9 사이라고 문제에서 보장됨
MAX_VALUE = 9


# 1️⃣ 카운트 배열 생성 (개수 저장용)
# 인덱스 = 값, 값 = 등장 횟수
cnt = [0] * (MAX_VALUE + 1)


# 2️⃣ 개수 세기 (빈도 집계)
# arr에 있는 값을 하나씩 보면서
# 해당 값의 개수를 증가시킨다
for x in arr:
    cnt[x] += 1
# 이 시점:
# cnt[v] = 값 v가 몇 번 나왔는지


# 3️⃣ 누적합 만들기 (자리 정보)
# 각 값이 "정렬된 결과에서 어디까지 차지하는지"
for i in range(1, len(cnt)):
    cnt[i] += cnt[i - 1]
# 이 시점:
# cnt[v] = 값 v가 들어갈 마지막 위치 + 1


# 4️⃣ 뒤에서부터 배치 (정렬 결과 생성)
# 안정 정렬을 위해 입력 배열의 뒤에서부터 처리
result = [0] * len(arr)

for i in range(len(arr) - 1, -1, -1):
    val = arr[i]
    cnt[val] -= 1          # 실제 들어갈 인덱스
    result[cnt[val]] = val