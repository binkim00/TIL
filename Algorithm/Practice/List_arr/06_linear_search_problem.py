####################################################
# [문제]
# 배열에 특정 값 V가 주어진다.
# V가 배열에 있으면 해당 인덱스를 출력하고,
# 없으면 -1을 출력하시오.
#
# [입력 예시]
# 1
# 5 7
# 2 4 7 1 3
#
# [출력 예시]
# #1 2
####################################################

T = int(input())
for test_case in range(1, T + 1):

    # N: 배열 길이, V: 찾을 값
    N, V = map(int, input().split())
    arr = list(map(int, input().split()))

    # 🔴 [놓친 부분 1]
    # "없으면 -1"은 '끝까지 다 찾아본 후'에 판단해야 함
    # → 그래서 미리 기본값을 -1로 둔다
    result = -1

    for i in range(N):
        # 🔴 [놓친 부분 2]
        # 문제는 '값'이 아니라 '인덱스'를 요구함
        # num == V 이면 num이 아니라 i를 써야 함
        if arr[i] == V:
            result = i # 인덱스 값이라 i 사용
            break
            # 🔴 [놓친 부분 3]
            # 찾았으면 더 볼 필요 없음 → break 필수

        # 🔴 [놓친 부분 4]
        # 여기서 바로 -1을 출력하면 안 됨
        # 첫 번째 원소가 아니라고 해서 "없다"고 확정할 수 없음
        # 그래서 else에서 print(-1)을 하면 오답이 됨

    # 🔴 [놓친 부분 5]
    # 출력은 for문 '안'이 아니라 '밖'에서 딱 한 번만
    # (문제 요구: 테스트케이스당 출력 1줄)
    print(f"#{test_case} {result}")
