# ==============================
# for문 ↔ 내장함수 초압축 정리
# ==============================

arr = [1, 2, 3, 4, 5]

# 1️⃣ 합 구하기
# for문 버전
total = 0
for x in arr:
    total += x

# 내장함수 버전 (조건 없으면 이걸로)
total = sum(arr)

###############################################################################################

# 2️⃣ 최대값 / 최소값
# for문 버전
max_val = arr[0]
for x in arr:
    if x > max_val:
        max_val = x

# 내장함수 버전
max_val = max(arr)
min_val = min(arr)

###############################################################################################

# 3️⃣ 개수 세기
# for문 버전
cnt = 0
for _ in arr:
    cnt += 1

# 내장함수 버전
cnt = len(arr)

###############################################################################################

# 4️⃣ 입력 처리 (시험에서 제일 많이 씀)
# for문 버전
arr = []
for x in input().split():
    arr.append(int(x))

# 내장함수 버전 (이건 거의 필수)
arr = list(map(int, input().split()))

###############################################################################################

# 5️⃣ 전부 같은 연산 적용 (선택)
# for문 버전
res = []
for x in arr:
    res.append(x * 2)

# 내장함수 버전
res = list(map(lambda x: x * 2, arr))


